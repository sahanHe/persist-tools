DROP TABLE IF EXISTS MultipleAssociations;
CREATE TABLE MultipleAssociations (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profileId VARCHAR,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_VARCHAR_0 FOREIGN KEY(profileId) REFERENCES VARCHAR(id),
	userId VARCHAR,
	CONSTRAINT FK_MULTIPLEASSOCIATIONS_VARCHAR_0 FOREIGN KEY(userId) REFERENCES VARCHAR(id),
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Users;
CREATE TABLE Users (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	profile Profile,
	PRIMARY KEY(id)
);

DROP TABLE IF EXISTS Profiles;
CREATE TABLE Profiles (
	id INT NOT NULL,
	name VARCHAR(191) NOT NULL,
	userId INT,
	CONSTRAINT FK_PROFILES_USER_0 FOREIGN KEY(userId) REFERENCES User(id),
	PRIMARY KEY(id)
);